package com.aurionpro.model;

public class ArithmeticOperators {

	public static void main(String[] args) {
		
		int number1 = 16;
        int number2 = 4;
      
        System.out.println("number1 + number2 = " + (number1 + number2));
        System.out.println("number1 - number2 = " + (number1 - number2));
        System.out.println("number1 * number2 = " + (number1 * number2));
        System.out.println("number1 / number2 = " + (number1 / number2));
        System.out.println("number1 % number2 = " + (number1 % number2));
          
    }
}